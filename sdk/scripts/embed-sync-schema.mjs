// Embeds the vendored canonical schema (schema/sync.schema.json) into the two
// runtimes that cannot read files at runtime: the browser SDK
// (sdk/src/sync-schema.mjs) and the Flutter package
// (dart/lib/src/sync_schema.dart). Node-stdlib-only, deterministic output,
// `--check` regenerates in memory and fails on drift (same pattern as the
// fiducia-interfaces generators). The Rust core needs no embed step — it
// `include_str!`s the schema file directly.
//
//   node sdk/scripts/embed-sync-schema.mjs          # regenerate
//   node sdk/scripts/embed-sync-schema.mjs --check  # drift gate (CI + npm test)

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..", "..");
export const SCHEMA_PATH = join(repoRoot, "schema", "sync.schema.json");
export const MJS_PATH = join(repoRoot, "sdk", "src", "sync-schema.mjs");
export const DART_PATH = join(repoRoot, "dart", "lib", "src", "sync_schema.dart");

/** Canonical pretty form both embeds share (2-space, trailing newline). */
export function canonicalSchemaText(rawText) {
  const parsed = JSON.parse(rawText);
  if (!parsed || typeof parsed !== "object" || !parsed.$defs) {
    throw new Error("schema/sync.schema.json must be an object with $defs");
  }
  return `${JSON.stringify(parsed, null, 2)}\n`;
}

export function renderMjs(schemaText) {
  return [
    "// GENERATED by sdk/scripts/embed-sync-schema.mjs from schema/sync.schema.json — DO NOT EDIT.",
    "// The vendored file mirrors fiducia-interfaces/schema/sync.schema.json byte for byte;",
    "// sdk/tests/schema-drift.test.mjs guards both copies.",
    "",
    `export const SYNC_SCHEMA = ${schemaText.trimEnd()};`,
    "",
  ].join("\n");
}

export function renderDart(schemaText) {
  if (schemaText.includes("'''")) {
    throw new Error("schema text may not contain a Dart raw-string terminator (''' )");
  }
  return [
    "// GENERATED by sdk/scripts/embed-sync-schema.mjs from schema/sync.schema.json — DO NOT EDIT.",
    "// The vendored file mirrors fiducia-interfaces/schema/sync.schema.json byte for byte.",
    "",
    "/// The canonical sync envelope JSON Schema, embedded verbatim.",
    "const String syncSchemaJson = r'''",
    schemaText.trimEnd(),
    "''';",
    "",
  ].join("\n");
}

export function generate() {
  const schemaText = canonicalSchemaText(readFileSync(SCHEMA_PATH, "utf8"));
  return [
    { path: MJS_PATH, content: renderMjs(schemaText) },
    { path: DART_PATH, content: renderDart(schemaText) },
  ];
}

function main() {
  const check = process.argv.includes("--check");
  let drifted = 0;
  for (const { path, content } of generate()) {
    if (check) {
      let current = null;
      try {
        current = readFileSync(path, "utf8");
      } catch {
        // Missing counts as drift.
      }
      if (current !== content) {
        drifted += 1;
        console.error(`DRIFT: ${path} is not what schema/sync.schema.json generates`);
      }
    } else {
      writeFileSync(path, content);
      console.log(`wrote ${path}`);
    }
  }
  if (check && drifted > 0) {
    console.error("run: node sdk/scripts/embed-sync-schema.mjs");
    process.exit(1);
  }
}

if (process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1]) {
  main();
}
